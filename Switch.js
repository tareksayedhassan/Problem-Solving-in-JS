function helo(number) {
  switch (number) {
    case 0:
      return "zero";
      break;
    case 1:
      return "one";
      break;
    case 2:
      return "two";
      break;
    case 3:
      return "Three";
      break;
    case 4:
      return "four";
      break;
    case 5:
      return "fife";
      break;
    case 6:
      return "six";
      break;
    case 7:
      return "seven";
      break;
    case 8:
      return "eghit";
      break;
    case 9:
      return "nine";
      break;
    default:
      return "wrong number";
  }
}
console.log(helo(3));
